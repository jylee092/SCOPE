"""
Decisive check: does removing the structural skip (D=0) preserve robustness
under missing logs? Compares SCOPE@D=0 to the existing SCOPE@D=2 robustness
numbers (0.45/0.49/0.44 at 10/25/50%).

Reuses the SAME dropped scenarios generated by the original Q2 run
(output/_robustness/drop{p}/seed0/) and the SAME on-disk description cache, so
this is deterministic and needs NO API. Writes to a distinct output dir
(scope_d0_drop*) so canonical/D2 outputs are untouched. Pipeline stdout is
silenced; results go to stderr.
"""
from __future__ import annotations
import json, sys, os, contextlib
from pathlib import Path
from statistics import mean

ROOT = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(ROOT))

import config
import experiments._robustness_run as rob
from pipeline.data_loader import load_and_normalize
from pipeline.rule_matcher import load_rules, run_grouping, merge_shared_supporting
from pipeline.feature_extractor import extract_all
from pipeline.feature_sanitizer import sanitize
from pipeline.mitre_mapper import analyze
from pipeline.attack_chain import (
    sort_results_by_time, load_tactic_map, build_group_nodes,
    TacticalScorer, get_semantic_scorer, CausalScorer,
    MultiDimTransitionScorer, load_campaign_library, topk_viterbi,
    apply_emission_confidence_bypass,
)
from pipeline.technique_io import load_or_build_technique_io
from experiments.attack_flows import get_flow
from experiments.chain_align import evaluate_chain_alignment

import os as _os
DROP_RATES = [float(x) for x in _os.environ.get("D0_DROPS", "0.25").split(",")]
SEED = int(_os.environ.get("D0_SEED", "0"))
MAX_SKIP = 0

_DN = open(os.devnull, "w", encoding="utf-8", errors="replace")


def log(*a):
    print(*a, file=sys.stderr, flush=True)


def _compute_one(dropped, stem, tactic_map, rule_list, campaigns, sem, cau, bypass_thr):
    with contextlib.redirect_stdout(_DN):
        df = load_and_normalize(str(dropped))
        groups = run_grouping(df=df, rule_list=rule_list,
                              before_sec=config.GROUPING_BEFORE_SEC,
                              after_sec=config.GROUPING_AFTER_SEC,
                              hop_up=config.GROUPING_HOP_UP,
                              hop_down=config.GROUPING_HOP_DOWN,
                              apply_filters=config.GROUPING_APPLY_FILTER,
                              use_shared_entity=config.GROUPING_USE_SHARED_ENTITY)
        groups = merge_shared_supporting(groups, df,
                                         overlap_threshold=config.MERGE_OVERLAP_THRESHOLD)
        if not groups:
            return []
        feats = extract_all(groups, df)
        ttp = analyze([sanitize(f) for f in feats], str(config.MITRE_CSV_PATH),
                      config.GEMINI_API_KEY, cache_dir=config.CACHE_DIR, use_llm=True)
        sr = sort_results_by_time(ttp, df)
        gnodes = build_group_nodes(sr, tactic_map, {f["group_id"]: f for f in feats})
        if not gnodes:
            return []
        multi = MultiDimTransitionScorer(
            tac_scorer=TacticalScorer(anomaly_threshold=config.TACTIC_ANOMALY_THRESHOLD),
            sem_scorer=sem, cau_scorer=cau,
            w_tac=config.W_TAC, w_sem=config.W_SEM, w_cau=config.W_CAU,
            self_loop_tid_penalty=getattr(config, "SELF_LOOP_TID_PENALTY", 1.0))
        vit = topk_viterbi(gnodes, multi, beam_k=config.VITERBI_BEAM_K,
                           max_skip=MAX_SKIP, skip_penalty=config.VITERBI_SKIP_PENALTY,
                           transition_weight=config.VITERBI_TRANSITION_WEIGHT,
                           campaigns=campaigns)
        if bypass_thr is not None:
            vit = apply_emission_confidence_bypass(vit, multi, sim_threshold=float(bypass_thr))
        return vit.score_breakdown


def run_drop(drop, tactic_map, rule_list, campaigns, sem, cau, bypass_thr):
    pct = int(round(drop * 100))
    out_root = config.OUTPUT_BASE_DIR / "_robustness" / f"scope_d0_drop{pct}_seed{SEED}"
    out = {}
    for orig in sorted(config.DATASET_FOLDER.rglob("*.json")):
        rel = orig.relative_to(config.DATASET_FOLDER).with_suffix("")
        stem = orig.stem
        dropped = rob._drop_path(drop, SEED, rel)
        if not dropped.exists():
            continue
        flow = get_flow(stem)
        if not flow:
            continue
        out_dir = out_root / rel
        out_dir.mkdir(parents=True, exist_ok=True)
        vit_path = out_dir / f"{stem}_viterbi.json"
        try:
            if not vit_path.exists():
                bd = _compute_one(dropped, stem, tactic_map, rule_list, campaigns,
                                  sem, cau, bypass_thr)
                json.dump(bd, open(vit_path, "w", encoding="utf-8"),
                          ensure_ascii=False, indent=2)
            bd = json.load(open(vit_path, encoding="utf-8"))
            out[stem] = (evaluate_chain_alignment(stem, bd, ref_flow=flow)
                         .get("technique_lcs_norm", 0.0)) if bd else 0.0
        except Exception as e:
            log(f"  [err] {stem}: {type(e).__name__}: {e}")
            out[stem] = 0.0
    return out


def main():
    with contextlib.redirect_stdout(_DN):
        tactic_map = load_tactic_map(str(config.MITRE_CSV_PATH))
        rule_list = load_rules(config.RULE_FOLDER)
        campaigns = load_campaign_library(str(config.CAMPAIGN_FOLDER), tactic_map)
        sem = get_semantic_scorer(getattr(config, "SEMANTIC_MODEL", config.CROSS_ENCODER_MODEL),
                                  backend=getattr(config, "SEMANTIC_BACKEND", "cross-encoder"),
                                  calibration=getattr(config, "SEM_CALIBRATION", "linear")) if config.USE_SEMANTIC_SCORING else None
        cau = CausalScorer(technique_io=load_or_build_technique_io(
            str(config.MITRE_CSV_PATH), cache_path=config.OUTPUT_BASE_DIR / "technique_io_cache.json")) if config.USE_CAUSAL_SCORING else None
    bypass_thr = getattr(config, "EMISSION_BYPASS_SIM_THRESHOLD", None)

    log("\n=== SCOPE @ D=0 robustness (vs existing D=2: 0.45/0.49/0.44) ===")
    log("  0% (complete, D=0) = 0.683  [known]")
    for drop in DROP_RATES:
        res = run_drop(drop, tactic_map, rule_list, campaigns, sem, cau, bypass_thr)
        m = mean(res.values()) if res else 0.0
        log(f"  {int(drop*100):>2}% drop, D=0: tech-LCS = {m:.4f}  (n={len(res)})")


if __name__ == "__main__":
    main()
